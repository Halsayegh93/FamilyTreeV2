-- نقل أوسع لإشعارات "إضافة ابن جديد" القديمة من kind='child_add'
-- → kind='admin_edit_child_add'
--
-- خلفية: migration 20260513210000 اشترطت body LIKE 'تمت إضافة%' وهذا
-- ضيّق وأهمل بعض النسخ القديمة التي صياغة body فيها مختلفة.
-- التمييز الكافي والآمن:
--   • title = 'إضافة ابن جديد' (ليس 'طلب إضافة ابن' بتاء التأنيث)
--   • request_id IS NULL (لا توجد طلب admin_request مرتبط — أي إجراء فوري لا طلب)
--
-- إشعارات طلبات المستخدم العادي تبقى kind='child_add' لأن:
--   • title = 'طلب إضافة ابن' (مختلف عن 'إضافة ابن جديد')
--   • request_id IS NOT NULL (مرتبطة بـadmin_request للموافقة)
-- ════════════════════════════════════════════════════════════════════════

UPDATE public.notifications
SET kind = 'admin_edit_child_add'
WHERE kind = 'child_add'
  AND title = 'إضافة ابن جديد'
  AND request_id IS NULL;
