-- ════════════════════════════════════════════════════════════════════════
-- نقل الإشعارات القديمة من kind='child_add' إلى kind='admin_edit_child_add'
-- لتظهر في تاب "المستجدات" بدل "إشعاراتي".
--
-- خلفية: قبل migration 20260513200000، الـtrigger السيرفري كان يرسل
-- إشعار kind='child_add' لكل إضافة عضو فيه father_id — بما فيها إضافات
-- المدير. هذه الإشعارات عالقة في تاب "إشعاراتي" بينما يجب أن تكون في
-- "المستجدات" كإجراءات منفّذة.
--
-- نمييز الإشعارات الـtrigger-generated (إضافات المدير) عن إشعارات
-- طلبات الانضمام بـ:
--   • title = 'إضافة ابن جديد' (ليس 'طلب إضافة ابن' بتاء التأنيث)
--   • request_id IS NULL (لا توجد طلب admin_request مرتبط)
--   • body يبدأ بـ'تمت إضافة'
--
-- إشعارات طلبات المستخدم العادي تبقى kind='child_add' للموافقة.
-- ════════════════════════════════════════════════════════════════════════

UPDATE public.notifications
SET kind = 'admin_edit_child_add'
WHERE kind = 'child_add'
  AND title = 'إضافة ابن جديد'
  AND (request_id IS NULL)
  AND body LIKE 'تمت إضافة%';
